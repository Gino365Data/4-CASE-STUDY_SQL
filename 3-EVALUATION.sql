CREATE TABLE EVALUATION (
CodeET VARCHAR(5) NOT NULL,
CodeMat VARCHAR(5) NOT NULL,
DateEval DATE,
Note DECIMAL(4,2),
PRIMARY KEY (CodeET,CodeMat),
FOREIGN KEY(CodeET) REFERENCES ETUDIANT(CodeET),
FOREIGN KEY(CodeMat) REFERENCES MATIERES(CodeMat)
);

